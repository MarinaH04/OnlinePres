<br>
Studentii Facultatii de Stiinte Economice se afla sub indrumarea unui corp de cadre didactice bine adaptat specificului 
programelor de invatamant din curricula universitara si deplin angajat in atingerea obiectivelor strategice ale Facultatii 
si Universitatii.<br><br>
 
In cei douazeci de ani care s-au scurs de la infiintarea Facultatii de Stiinte Economice corpul de cadre didactice a 
crescut continuu, atragand in randurile sale, alaturi de absolventi cei mai buni ai diferitelor promotii, o serie de 
profesionisti din afara universitatii, fapt ce a asigurat  echilibrul - atat de necesar unei institutii in proces de 
formare - intre zelul tinerilor si experienta seniorilor. Pe de o parte, specialistii din diferite domenii (banci, 
companii, institutii ale statului s.a.) care ni s-au alaturat au ajutat la integrarea Facultătii în peisajul economic al 
zonei Sibiului si in general, in problemele economiei romanesti; pe de alta parte, tinerii absolventi care au optat pentru
 cariera universitara au adus cu ei ritm, prospetime, pasiune, daruire...In ultimii zece ani, peste 55 la suta din numarul
 cadrelor didactice aveau varsta de pana in patruzeci si cinci de ani.<br><br> 
 
In decursul anilor, corpul academic al Facultatii a incercat - atat prin dimensiunile cat si prin structura sa, sa 
raspunda cat mai bine ofertei educationale, aflata intr-o permanenta diversificare si totodata exigentelor calitative 
impuse de reforma invatamantului si cercetarii din Romania. Actualmente, in Facultatea de Stiinte Economice isi desfasoara
 activitatea un număr de 53 de cadre didactice titulare si 35 de colaboratori. Remarcabil este faptul ca dintre tinerele 
cadre didactice recrutate de-a lungul anilor din rândul absolventilor - reprezentand 49 la suta din totalul cadrelor 
didactice - 85 la suta au urmat cursurile facultatii noastre.<br><br>
 
Corpul cadrelor didactice din Facultatea de Stiinte Economice este structurat in doua departamente, care functioneaza ca 
subdiviziuni autonome, fiecare dintre ele acoperind o arie larga a domeniului stiintelor economice, in stransa concordanta
 cu oferta educationala si reforma curriculara. Gruparea cadrelor didactice in departamente este menita a servi 
obiectivelor generale ale facultatii: cresterea calitatii invatamantului si cercetarii, gestionarea optima a programelor 
de studii, masurarea performantei, stimularea competentei profesionale la nivel individual si de grup etc.
